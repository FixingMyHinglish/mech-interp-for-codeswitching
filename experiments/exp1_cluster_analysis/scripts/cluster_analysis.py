#!/usr/bin/env python3
"""Ward clustering on one layer's neuron summary."""

from __future__ import annotations

import argparse
import warnings
from pathlib import Path

import numpy as np
import pandas as pd
from scipy.cluster.hierarchy import fcluster, linkage
from sklearn.metrics import silhouette_score
from sklearn.preprocessing import StandardScaler

warnings.filterwarnings("ignore")

FEATURES = ["mean_code_switched", "mean_target", "delta_cs", "delta_target"]
SUMMARY_COLS = ["mean_english", "mean_code_switched", "mean_target", "delta_cs", "delta_target"]


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser()
    p.add_argument("--work_dir", type=Path, required=True)
    p.add_argument("--layer_num", type=int, required=True)
    p.add_argument("--k", type=int, default=200)
    p.add_argument(
        "--auto_k",
        action="store_true",
        help="If set, choose k by maximizing silhouette score over --k_min..--k_max.",
    )
    p.add_argument("--k_min", type=int, default=2)
    p.add_argument("--k_max", type=int, default=10)
    p.add_argument("--linkage_method", default="ward")
    return p.parse_args()


def load_neuron_summary(work_dir: Path, layer: int) -> pd.DataFrame:
    path = work_dir / "03_cleaned" / f"layer_{layer:02d}_neuron_summary.csv"
    if not path.exists():
        raise FileNotFoundError(path)
    df = pd.read_csv(path)
    required = set(SUMMARY_COLS) | {"neuron_id", "layer"}
    if missing := required - set(df.columns):
        raise ValueError(f"CSV missing columns: {missing}")
    return df.dropna(subset=list(required))


def run_ward_linkage(df: pd.DataFrame, linkage_method: str = "ward") -> np.ndarray:
    X = StandardScaler().fit_transform(df[FEATURES].values)
    return linkage(X, method=linkage_method, metric="euclidean")


def standardized_feature_matrix(df: pd.DataFrame) -> np.ndarray:
    return StandardScaler().fit_transform(df[FEATURES].values)


def choose_best_k_by_silhouette(
    X: np.ndarray, Z: np.ndarray, n_samples: int, k_min: int, k_max: int
) -> tuple[int, dict[int, float]]:
    k_min = max(2, int(k_min))
    k_max = min(int(k_max), n_samples - 1)
    if k_min > k_max:
        raise ValueError(f"Invalid k range after bounds: k_min={k_min}, k_max={k_max}, n={n_samples}")

    scores: dict[int, float] = {}
    for k in range(k_min, k_max + 1):
        labels = labels_for_k(Z, k, n_samples)
        scores[k] = float(silhouette_score(X, labels, metric="euclidean"))
    best_k = max(scores, key=scores.get)
    return int(best_k), scores


def labels_for_k(Z: np.ndarray, k: int, n_samples: int) -> np.ndarray:
    if k < 2 or k >= n_samples:
        raise ValueError(f"Need 2 <= k < n_samples; got k={k}, n={n_samples}")
    return fcluster(Z, k, criterion="maxclust")


def cluster_summary(df: pd.DataFrame, labels: np.ndarray) -> pd.DataFrame:
    d = df.assign(cluster=labels)
    s = d.groupby("cluster")[SUMMARY_COLS].mean()
    s["n_neurons"] = d.groupby("cluster")["neuron_id"].count()
    return s


def export_cluster_summary_readme(
    work_dir: Path,
    *,
    layer: int,
    k: int,
    n_neurons: int,
    summary: pd.DataFrame,
    linkage_method: str,
    auto_scores: dict[int, float] | None = None,
) -> Path:
    out_dir = work_dir / "04_analysis"
    out_dir.mkdir(parents=True, exist_ok=True)
    out_path = out_dir / f"README_cluster_summary_layer{layer:02d}_k{k}.md"

    summary = summary.copy()
    summary["cluster_share"] = summary["n_neurons"] / int(n_neurons)
    largest_share = float(summary["cluster_share"].max())
    mean_abs_gap = float((summary["delta_cs"] - summary["delta_target"]).abs().mean())

    lines: list[str] = []
    lines.append(f"# Cluster summary ({work_dir.name})\n\n")
    lines.append("*Auto-generated by `cluster_analysis.py`*\n\n")
    lines.append(
        "Ward clustering on standardized "
        "`mean_code_switched`, `mean_target`, `delta_cs`, `delta_target`.\n\n"
    )
    lines.append(f"- **Layer**: **{layer}**\n")
    lines.append(f"- **Clusters (`k`)**: **{k}**\n")
    lines.append(f"- **Linkage method**: **{linkage_method}**\n")
    lines.append(f"- **Neurons clustered**: **{n_neurons}**\n")
    lines.append(f"- **Largest cluster share**: **{largest_share:.2%}**\n")
    lines.append(f"- **Mean cluster abs gap** `|delta_cs - delta_target|`: **{mean_abs_gap:.6f}**\n")
    lines.append("---\n\n")

    if auto_scores:
        lines.append("## Auto-k candidates\n\n")
        lines.append("| k | silhouette |\n")
        lines.append("| --- | --- |\n")
        for kk, sc in sorted(auto_scores.items(), key=lambda kv: kv[1], reverse=True):
            mark = " <- selected" if int(kk) == int(k) else ""
            lines.append(f"| {int(kk)} | {float(sc):.6f}{mark} |\n")
        lines.append("\n")

    disp = summary.reset_index()
    disp["cluster"] = disp["cluster"].astype(int)
    disp["n_neurons"] = disp["n_neurons"].astype(int)
    disp["cluster_share"] = disp["cluster_share"].map(lambda x: f"{float(x):.2%}")
    for col in SUMMARY_COLS:
        disp[col] = disp[col].map(lambda x: f"{float(x):.6f}")

    cols = ["cluster", "n_neurons", "cluster_share", *SUMMARY_COLS]
    lines.append("## Cluster table\n\n")
    lines.append("| " + " | ".join(cols) + " |\n")
    lines.append("| " + " | ".join(["---"] * len(cols)) + " |\n")
    for _, row in disp.iterrows():
        lines.append("| " + " | ".join(str(row[c]) for c in cols) + " |\n")
    lines.append("\n")

    out_path.write_text("".join(lines), encoding="utf-8")
    return out_path


def main() -> None:
    args = parse_args()
    work_dir = args.work_dir.resolve()
    layer = int(args.layer_num)

    df = load_neuron_summary(work_dir, layer)
    n = len(df)
    X = standardized_feature_matrix(df)
    Z = linkage(X, method=str(args.linkage_method), metric="euclidean")

    if bool(args.auto_k):
        k, scores = choose_best_k_by_silhouette(X, Z, n, k_min=args.k_min, k_max=args.k_max)
        top = sorted(scores.items(), key=lambda kv: kv[1], reverse=True)[:5]
        print("Auto-k (silhouette) top candidates:")
        for kk, sc in top:
            print(f"  k={kk}: silhouette={sc:.6f}")
    else:
        k = int(args.k)

    labels = labels_for_k(Z, k, n)
    summary = cluster_summary(df, labels).round(6)

    out_dir = work_dir / "04_analysis"
    out_dir.mkdir(parents=True, exist_ok=True)
    out_csv = out_dir / f"cluster_summary_layer{layer:02d}_k{k}.csv"
    summary.reset_index().to_csv(out_csv, index=False)
    out_md = export_cluster_summary_readme(
        work_dir,
        layer=layer,
        k=k,
        n_neurons=n,
        summary=summary,
        linkage_method=str(args.linkage_method),
        auto_scores=scores if bool(args.auto_k) else None,
    )

    print(f"Layer {layer} neurons: {n}, k={k}")
    print(summary.to_string())
    print(f"\nSaved → {out_csv}")
    print(f"Saved → {out_md}")


if __name__ == "__main__":
    main()

