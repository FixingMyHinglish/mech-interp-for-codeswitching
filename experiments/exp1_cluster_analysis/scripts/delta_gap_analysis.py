#!/usr/bin/env python3
"""CS vs target gap analysis; `--write-readme` exports tables + intersection CSV."""

from __future__ import annotations

import argparse
import warnings
from pathlib import Path

import pandas as pd

import cluster_analysis as ca

warnings.filterwarnings("ignore")


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser()
    p.add_argument("--work_dir", type=Path, required=True)
    p.add_argument("--layer_num", type=int, required=True)
    p.add_argument("--ks", type=int, nargs="+", default=[10, 20, 30, 40, 50])
    p.add_argument("--write-readme", action="store_true")
    return p.parse_args()


def neuron_level_baseline(df: pd.DataFrame) -> tuple[float, float]:
    abs_diff = (df["delta_cs"] - df["delta_target"]).abs()
    m = float(abs_diff.mean())
    return m, 3.0 * m


def neuron_ids_in_flagged_clusters(df: pd.DataFrame, Z, k: int, thr3_glob: float) -> set[int]:
    labels = ca.labels_for_k(Z, k, len(df))
    s = ca.cluster_summary(df, labels)
    s["abs_gap"] = (s["delta_cs"] - s["delta_target"]).abs()
    bad = set(s[s["abs_gap"] > thr3_glob].index.astype(int))
    d = df.assign(cluster=labels)
    return set(d.loc[d["cluster"].isin(bad), "neuron_id"].astype(int))


def dataframe_neurons_flagged_all_k(df: pd.DataFrame, Z, ks: list[int], thr3_glob: float) -> pd.DataFrame:
    sets = [neuron_ids_in_flagged_clusters(df, Z, k, thr3_glob) for k in ks]
    inter = set.intersection(*sets) if sets else set()
    abs_n = (df["delta_cs"] - df["delta_target"]).abs()
    out = df[df["neuron_id"].isin(inter)].copy()
    out["neuron_abs_gap"] = abs_n.loc[out.index].values
    out["neuron_level_gt_global"] = out["neuron_abs_gap"] > thr3_glob
    return out.sort_values("neuron_id")


def export_gap_threshold_readme(work_dir: Path, layer_num: int, ks: list[int], out_path: Path | None = None) -> Path:
    out_dir = work_dir / "04_analysis"
    out_dir.mkdir(parents=True, exist_ok=True)
    out_path = out_path or (out_dir / "README_gap_threshold_clusters.md")

    df = ca.load_neuron_summary(work_dir, layer_num)
    gmean, thr3_glob = neuron_level_baseline(df)
    Z = ca.run_ward_linkage(df)

    lines: list[str] = []
    lines.append(f"# Gap-threshold clusters ({work_dir.name})\n\n")
    lines.append("*Auto-generated by `delta_gap_analysis.py --write-readme`*\n\n")
    lines.append(
        "Ward linkage on standardized `mean_code_switched`, `mean_target`, "
        "`delta_cs`, `delta_target`. **`abs_gap`** = |mean(ΔCS) − mean(Δtarget)| per cluster.\n\n"
    )
    lines.append(f"- **Layer**: **{layer_num}**\n")
    lines.append(f"- **Neuron-level mean** |ΔCS − Δtarget|: **{gmean:.6f}**\n")
    lines.append(f"- **Global threshold** (3×): **{thr3_glob:.6f}**\n")
    lines.append("---\n\n")

    for k in ks:
        if k < 2 or k >= len(df):
            continue
        labels = ca.labels_for_k(Z, k, len(df))
        s = ca.cluster_summary(df, labels)
        s["abs_gap"] = (s["delta_cs"] - s["delta_target"]).abs()
        row_mean = float(s["abs_gap"].mean())
        thr3_row = 3.0 * row_mean
        s["ratio_vs_neuron_mean"] = s["abs_gap"] / gmean
        s["ratio_vs_3x_global"] = s["abs_gap"] / thr3_glob
        s["opposite_sign_deltas"] = (s["delta_cs"] * s["delta_target"]) < 0
        hi_glob = s[s["abs_gap"] > thr3_glob].sort_values("abs_gap", ascending=False)
        hi_row = s[s["abs_gap"] > thr3_row]

        lines.append(f"## k = {k}\n\n")
        lines.append(f"- Mean `abs_gap` across clusters: **{row_mean:.6f}**  \n")
        lines.append(f"- Within-k threshold (3×): **{thr3_row:.6f}**  \n")
        lines.append(f"- **Outside global threshold:** **{len(hi_glob)}** clusters  \n")
        lines.append(f"- **Outside within-k threshold:** **{len(hi_row)}** clusters  \n\n")

        disp = hi_glob.reset_index()
        cols = [
            "cluster",
            "n_neurons",
            "mean_english",
            "mean_code_switched",
            "mean_target",
            "delta_cs",
            "delta_target",
            "abs_gap",
            "ratio_vs_neuron_mean",
            "ratio_vs_3x_global",
            "opposite_sign_deltas",
        ]
        for c in [
            "mean_english",
            "mean_code_switched",
            "mean_target",
            "delta_cs",
            "delta_target",
            "abs_gap",
            "ratio_vs_neuron_mean",
            "ratio_vs_3x_global",
        ]:
            disp[c] = disp[c].map(lambda x: f"{float(x):.6f}")
        disp["cluster"] = disp["cluster"].astype(int)
        disp["n_neurons"] = disp["n_neurons"].astype(int)
        disp["opposite_sign_deltas"] = disp["opposite_sign_deltas"].map({True: "yes", False: "no"})

        lines.append("| " + " | ".join(cols) + " |\n")
        lines.append("| " + " | ".join(["---"] * len(cols)) + " |\n")
        for _, r in disp.iterrows():
            lines.append("| " + " | ".join(str(r[c]) for c in cols) + " |\n")
        lines.append("\n")

    inter_df = dataframe_neurons_flagged_all_k(df, Z, ks, thr3_glob)
    csv_path = out_dir / "neurons_flagged_cluster_all_k.csv"
    inter_df.to_csv(csv_path, index=False)

    lines.append("---\n\n")
    lines.append("## Neurons in an above-threshold cluster for **every** k\n\n")
    lines.append(f"Intersection across k ∈ {ks}: **{len(inter_df)}** neurons.\n\n")
    lines.append(f"- **CSV:** `{csv_path.name}`\n")

    out_path.write_text("".join(lines), encoding="utf-8")
    return out_path


def main() -> None:
    args = parse_args()
    work_dir = args.work_dir.resolve()
    layer_num = int(args.layer_num)
    ks = [int(k) for k in args.ks]

    df = ca.load_neuron_summary(work_dir, layer_num)
    gmean, thr3_glob = neuron_level_baseline(df)
    print(f"Neuron-level mean |delta_cs - delta_target|: {gmean:.6f}")
    print(f"3× threshold: {thr3_glob:.6f}\n")
    Z = ca.run_ward_linkage(df)
    for k in ks:
        if k < 2 or k >= len(df):
            continue
        labels = ca.labels_for_k(Z, k, len(df))
        s = ca.cluster_summary(df, labels)
        s["abs_gap"] = (s["delta_cs"] - s["delta_target"]).abs()
        print(f"k={k}: clusters with abs_gap > global: {len(s[s['abs_gap'] > thr3_glob])}")

    if args.write_readme:
        p = export_gap_threshold_readme(work_dir, layer_num, ks)
        print(f"\nWrote {p}")
        print(f"Wrote {work_dir / '04_analysis' / 'neurons_flagged_cluster_all_k.csv'}")


if __name__ == "__main__":
    main()

